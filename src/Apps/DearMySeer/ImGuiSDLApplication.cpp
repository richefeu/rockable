void ImGuiSDLApplication::LoadTextureFromMemory(unsigned char data[], unsigned int len, GLuint& textureID) {
  int width, height, channels;
  // Décoder l'image depuis le tableau
  unsigned char* pixels = stbi_load_from_memory(data, len, &width, &height, &channels,
                                                STBI_rgb_alpha  // Force 4 canaux (RGBA)
  );

  if (!pixels) {
    std::cout << "pixels not loaded with 'stbi_load_from_memory'" << std::endl;
    return;
  }

  // Créer une texture OpenGL
  glGenTextures(1, &textureID);
  glBindTexture(GL_TEXTURE_2D, textureID);
  glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, pixels);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
  glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

  // Libérer la mémoire allouée par stb_image
  stbi_image_free(pixels);
}

