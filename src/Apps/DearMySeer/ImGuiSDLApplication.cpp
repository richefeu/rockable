void ImGuiSDLApplication::mainLoop() {

  while (!shouldQuit) {
    handleEvents();

    // Début du frame ImGui
    ImGui_ImplOpenGL3_NewFrame();
    ImGui_ImplSDL2_NewFrame();
    ImGui::NewFrame();

    mainMenu();

    if (show_main_panel) {
      mainPanel();
    }

    if (show_slicing_panel) {
      slicingPanel();
    }

    if (show_screenshot_panel) {
      screenshotPanel();
    }

    if (show_config_panel) {
      configPanel();
    }

    if (show_window_info) {
      infoPanel();
    }

    if (should_save_screenshot_series) {
      for (confNum = screenshot_first; confNum <= screenshot_last; confNum++) {
        if (confNum < 0) confNum = 0;
        if (tryToReadConf(confNum) == false) {
          confNum = lastConfNumOK;
        }
        snprintf(screenshot_filename, 256, "%s%04d.png", screenshot_basename, confNum);
        should_save_screenshot = true;
        renderFrame();
      }
      should_save_screenshot_series = false;
    } else {
      renderFrame();
    }
  }
}

/// Point d'entrée principal de l'application
int ImGuiSDLApplication::run() {
  if (!init()) {
    return 1;
  }

  mainLoop();

  return 0;
}

