bool ImGuiSDLApplication::SaveScreenshotWithoutImGui(const char* filename, int W, int H) {
  // Allouer un buffer pour stocker les pixels
  unsigned char* pixels = new unsigned char[W * H * 3];

  // Lire les pixels depuis le framebuffer
  glReadPixels(0, 0, W, H, GL_RGB, GL_UNSIGNED_BYTE, pixels);

  // Inverser les lignes (OpenGL stocke les pixels de bas en haut)
  unsigned char* flipped_pixels = new unsigned char[W * H * 3];
  for (int y = 0; y < H; y++) {
    memcpy(flipped_pixels + (H - 1 - y) * W * 3, pixels + y * W * 3, W * 3);
  }

  // Sauvegarder l'image
  int result = stbi_write_png(filename, W, H, 3, flipped_pixels, W * 3);

  // Libérer la mémoire
  delete[] pixels;
  delete[] flipped_pixels;

  return (result != 0);
}

